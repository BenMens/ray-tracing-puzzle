package nl.basmens.game;

import nl.basmens.renderer.Camera;
import org.joml.Vector3f;

public class Player {
  private Camera camera;


  // ===============================================================================================
  // Constructor
  // ===============================================================================================
  public Player() {}

  // ===============================================================================================
  // Init
  // ===============================================================================================
  public void init() {
    camera = new Camera(new Vector3f(0), new Vector3f(0), 3F);
  }

  // ===============================================================================================
  // Getters and setters
  // ===============================================================================================
  public Camera getCamera() {
    return camera;
  }
}
